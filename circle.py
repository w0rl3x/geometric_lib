import math

def area(r):
    '''Возвращает число pi умноженное на квадрат r, принимает десятичное число r'''
    return math.pi * r * r


def perimeter(r):
    '''Возвращает десятичное число "2" умноженное на число pi и умноженное на значение переменной r, принимает десятичное число r '''
    return 2 * math.pi * r

